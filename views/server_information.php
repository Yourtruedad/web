<h1>Server Information</h1>
<p class="lead">The most important information about our server EverWinter MU</p>

<p><strong>Version:</strong> S9Ep2</p>
<p><strong>Server Files:</strong> Premium Plus IGCN</p>
<p><strong>Max level:</strong> 400</p>
<p>
    <strong>Exp:</strong> Dynamic, basic 27x<br>
    Every reset lowers the exp. rate by 1%. If you have more than 25 resets, the exp. rate lowers by 2%.
</p>
<p><strong>MasterExp:</strong> 13x</p>
<p><strong>Drop:</strong> 31%</p>
<p>
    <strong>Reset:</strong> 
    <ul>
        <li>Required level 400.</li>
        <li>Paid 70,000,000.</li>
        <li>Statistics are reset.</li>
        <li>You get 300 Level Up Points per reset.</li>
        <li>Inventory stays.</li>
        <li>Master Level and the Master Skill tree are reset.</li>
        <li>Up to 50 resets.</li>
    </ul>
</p>
<p>
    <strong>Grand Reset:</strong>
    <ul>
        <li>Required level 400 and 50 resets.</li>
        <li>Paid 100,000,000.</li>
        <li>Statistics are reset. You get 1000 points.</li>
        <li>Up to 2 GRs.</li>
        <li>You get 10.000 goblin points.</li>
    </ul>
Goblin Points can be exchanged for items that do not drop (i.e. AA Sword non-excellent +13+28+L+S that is SoulBound and you cannot sell it. or the Hell Night Armor +13+28+L+S). What's the advantage of being able to do only 2 grand resets? You cannot get a complete set just for doing GRs. You can get 2 but the rest will need to be found in the game.
</p>
